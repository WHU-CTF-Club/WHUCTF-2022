ans = ''

for i in range(len(code)):
	if i % 2 == 1:
		ans += chr(code[i] ^ i)
	else:
		ch = code[i]
		if ch >= ord('a') and ch <= ord('z'):
			ans += chr((ch - ord('a') + 13) % 26 + ord('a'))
		elif ch >= ord('A') and ch <= ord('Z'):
			ans += chr((ch - ord('A') + 13) % 26 + ord('A'))
		else:
			ans += chr(ch)

print(ans)
